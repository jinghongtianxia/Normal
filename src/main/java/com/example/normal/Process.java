public class Process {

    public static int addProcess(int a, int b) {
        int c = a + b;
        return c;
    }

    public static int divideProcess(int a, int b) {
        int c = a/b;
        return c;
    }

    public static int multiplyProcess(int a, int b) {
        int c = a * b;
        return c;
    }

    public static int minusProcess(int a, int b) {
        int c = a - b;
        return c;
    }

}
